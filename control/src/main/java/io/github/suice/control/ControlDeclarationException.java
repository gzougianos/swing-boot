package io.github.suice.control;

public class ControlDeclarationException extends RuntimeException {
	private static final long serialVersionUID = -7712534813204714622L;

	public ControlDeclarationException() {
		super();
	}

	public ControlDeclarationException(String message, Throwable cause) {
		super(message, cause);
	}

	public ControlDeclarationException(String message) {
		super(message);
	}

	public ControlDeclarationException(Throwable cause) {
		super(cause);
	}

}
